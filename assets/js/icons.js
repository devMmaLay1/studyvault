/**
 * Initialize Lucide icons on the page
 * Call this after DOM content is loaded or after dynamic content is added
 */
function initIcons() {
  if (window.lucide) {
    lucide.createIcons();
  }
}

/**
 * Get a Lucide icon as an HTML string
 * @param {string} name - Icon name (e.g., 'home', 'book', 'check')
 * @param {string} className - Optional CSS classes
 * @param {number} size - Icon size in pixels (default: 24)
 * @returns {string} HTML string for the icon
 */
function getLucideIcon(name, className = '', size = 24) {
  return `<i data-lucide="${name}" class="${className}" style="width: ${size}px; height: ${size}px;"></i>`;
}

